#math methods

import math

pi = 3.14
x, y, z = 1, 2, 3

print(round(pi)) #truncate or rounding down
print(math.ceil(pi)) #rounding up
print(math.floor(pi)) #rouding down
print(abs(pi)) #absolute value
print(pow(pi,2)) #pow to the second parameters exponent
print(math.sqrt(pi)) #square only positive of course
print(max(x,y,z))
print(min(x,y,z))
